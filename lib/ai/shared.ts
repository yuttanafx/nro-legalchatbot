export function buildUserPrompt(params: {
  userMessage: string;
  contextBlock: string;
  categoryNameTh: string;
}): string {
  return `หมวดกฎหมายที่เกี่ยวข้อง: ${params.categoryNameTh}

ข้อมูลอ้างอิงจากคลังความรู้:
${params.contextBlock}

คำถามจากผู้ใช้:
${params.userMessage}`;
}

export type AskParams = {
  userMessage: string;
  contextBlock: string;
  categoryNameTh: string;
};

