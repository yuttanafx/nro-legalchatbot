export const BOT_TONE_OPTIONS = ["formal", "professional_friendly", "warm_casual"] as const;
export type BotTone = (typeof BOT_TONE_OPTIONS)[number];

const TONE_INSTRUCTIONS: Record<BotTone, string> = {
  formal: `น้ำเสียงในการตอบ: เป็นทางการแบบหน่วยงานราชการ ใช้คำสุภาพเต็มรูปแบบ (เช่น "ท่าน", "ทั้งนี้", "โปรดทราบ")
กระชับ ตรงประเด็น หลีกเลี่ยงคำลงท้ายกันเองหรืออิโมจิ เน้นความน่าเชื่อถือและความแม่นยำเป็นหลัก`,
  professional_friendly: `น้ำเสียงในการตอบ: มืออาชีพแต่เป็นมิตร สุภาพ อบอุ่นเล็กน้อย เข้าใจง่าย ใช้คำลงท้าย "ครับ/ค่ะ" ตามความเหมาะสม
ไม่แข็งทื่อจนเกินไป แต่ยังคงความน่าเชื่อถือแบบมืออาชีพ (นี่คือโทนมาตรฐาน)`,
  warm_casual: `น้ำเสียงในการตอบ: เป็นกันเอง อบอุ่น เหมือนคุยกับคนที่ห่วงใยและเข้าใจว่าเรื่องกฎหมายอาจสร้างความกังวลใจ
ใช้ภาษาที่เข้าถึงง่าย พูดคุยเป็นธรรมชาติ แสดงความเห็นอกเห็นใจต่อสถานการณ์ของผู้ถามก่อนอธิบายข้อมูล
แต่ยังคงความสุภาพและความแม่นยำของข้อมูลกฎหมายไว้เหมือนเดิม ไม่ใช้คำแสลงหรือลดความน่าเชื่อถือของเนื้อหา`
};

export function buildSystemPrompt(tone?: string): string {
  const resolvedTone: BotTone = BOT_TONE_OPTIONS.includes(tone as BotTone)
    ? (tone as BotTone)
    : "professional_friendly";
  return `${BASE_SYSTEM_PROMPT}\n\n${TONE_INSTRUCTIONS[resolvedTone]}`;
}

